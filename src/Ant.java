

public class Ant {
    private int id; // id
    private float x; // x - coordinate
    private float y; // y = coordinate
    public Ant(int id , float x ,float y){
        this.id = id;
        this.x = x;
        this.y = y;
    }

    public int getId() {
        return id;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
