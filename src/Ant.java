

public class Ant {
    private int id; // id
    private int x; // x - coordinate
    private int y; // y = coordinate
    public Ant(int id , int x ,int y){
        this.id = id;
        this.x = x;
        this.y = y;
    }

    public int getId() {
        return id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
